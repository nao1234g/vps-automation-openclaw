#!/usr/bin/env python3
"""generate_regression_from_failure.py

failure_memory.json を読んで、各 failure の required_test フィールドから
regression test ファイル (scripts/eval/tests/failure_regressions/test_FXXX.py) を生成する。

Usage:
    python scripts/eval/generate_regression_from_failure.py
    python scripts/eval/generate_regression_from_failure.py --force   # 既存ファイルを上書き
    python scripts/eval/generate_regression_from_failure.py --dry-run # 生成内容を標準出力のみ

生成したファイルは failure_regression_index.json に記録される。
"""

import argparse
import json
import os
import sys
from datetime import datetime, timezone

_HERE = os.path.dirname(os.path.abspath(__file__))
_PROJECT_ROOT = os.environ.get(
    "CLAUDE_PROJECT_DIR",
    os.path.abspath(os.path.join(_HERE, "..", ".."))
)

_FAILURE_MEMORY_PATH = os.path.join(_PROJECT_ROOT, ".claude", "state", "failure_memory.json")
_REGRESSION_INDEX_PATH = os.path.join(_PROJECT_ROOT, ".claude", "state", "failure_regression_index.json")
_TEST_DIR = os.path.join(_PROJECT_ROOT, "scripts", "eval", "tests", "failure_regressions")

# テストファイルテンプレート（最小構成）
_TEMPLATE = '''\
"""{failure_id} Regression Test — {title}

Failure: {failure_id} ({severity}) — {category}
Symptom: {symptom}
Required test: {required_test}

AUTO-GENERATED by generate_regression_from_failure.py
DO NOT EDIT — edit the generator or create a custom override instead.
"""
import sys
import os

_HERE = os.path.dirname(os.path.abspath(__file__))
_PROJECT_ROOT = os.path.abspath(os.path.join(_HERE, "..", "..", "..", ".."))
sys.path.insert(0, _PROJECT_ROOT)


def test() -> bool:
    """
    {failure_id}: {required_test}
    """
    # TODO: このスタブを実際のテストコードに置き換えてください。
    # required_test: {required_test}
    print(f"[SKIP] {failure_id}: テストスタブ（未実装）")
    return True  # 未実装はSKIPとしてPASS扱い


if __name__ == "__main__":
    sys.exit(0 if test() else 1)
'''


def _load_failure_memory() -> dict:
    if not os.path.exists(_FAILURE_MEMORY_PATH):
        print(f"[ERROR] failure_memory.json が見つからない: {_FAILURE_MEMORY_PATH}", file=sys.stderr)
        sys.exit(1)
    with open(_FAILURE_MEMORY_PATH, encoding="utf-8") as f:
        return json.load(f)


def _load_index() -> dict:
    if os.path.exists(_REGRESSION_INDEX_PATH):
        with open(_REGRESSION_INDEX_PATH, encoding="utf-8") as f:
            return json.load(f)
    return {
        "_schema_version": "1.0",
        "_description": "failure_memory.json の各失敗に対応する regression test の管理インデックス",
        "generated_by": "scripts/eval/generate_regression_from_failure.py",
        "failures": []
    }


def _save_index(data: dict) -> None:
    os.makedirs(os.path.dirname(_REGRESSION_INDEX_PATH), exist_ok=True)
    with open(_REGRESSION_INDEX_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


def _generate_test_content(failure: dict) -> str:
    fid = failure["failure_id"]
    required_test = failure.get("required_test", "（required_testフィールドなし）")

    # title: symptomの先頭60文字
    title = failure.get("symptom", "")[:60]

    return _TEMPLATE.format(
        failure_id=fid,
        title=title,
        severity=failure.get("severity", "unknown"),
        category=failure.get("category", "unknown"),
        symptom=failure.get("symptom", "")[:120],
        required_test=required_test[:200],
    )


def _get_test_path(failure_id: str) -> str:
    return os.path.join(_TEST_DIR, f"test_{failure_id}.py")


def _update_index(index: dict, failure: dict, test_path: str) -> None:
    """index の failures リストを更新（同じ failure_id は上書き）"""
    fid = failure["failure_id"]
    rel_path = os.path.relpath(test_path, _PROJECT_ROOT).replace("\\", "/")

    # 既存エントリを検索
    existing = next((e for e in index["failures"] if e["failure_id"] == fid), None)

    if existing:
        existing["test_file"] = rel_path
        existing["generated_at"] = datetime.now(timezone.utc).isoformat()
        existing["severity"] = failure.get("severity", "unknown")
        existing["resolved_status"] = failure.get("resolved_status", "open")
    else:
        index["failures"].append({
            "failure_id": fid,
            "test_file": rel_path,
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "last_run": None,
            "last_result": None,
            "severity": failure.get("severity", "unknown"),
            "resolved_status": failure.get("resolved_status", "open"),
        })


def main() -> None:
    parser = argparse.ArgumentParser(description="failure_memory.json からregression testを生成")
    parser.add_argument("--force", action="store_true", help="既存テストファイルを上書き")
    parser.add_argument("--dry-run", action="store_true", help="ファイルを書かずに生成内容を表示")
    parser.add_argument("--failure-id", help="特定のfailure_idだけ生成（例: F001）")
    args = parser.parse_args()

    data = _load_failure_memory()
    failures = data.get("failures", [])

    if args.failure_id:
        failures = [f for f in failures if f["failure_id"] == args.failure_id]
        if not failures:
            print(f"[ERROR] failure_id={args.failure_id} が見つからない", file=sys.stderr)
            sys.exit(1)

    index = _load_index()
    os.makedirs(_TEST_DIR, exist_ok=True)

    generated = 0
    skipped = 0

    for failure in failures:
        fid = failure["failure_id"]
        test_path = _get_test_path(fid)

        if os.path.exists(test_path) and not args.force:
            print(f"[SKIP] {fid}: {os.path.basename(test_path)} は既存（--force で上書き）")
            skipped += 1
            # 既存でもインデックスは更新
            _update_index(index, failure, test_path)
            continue

        content = _generate_test_content(failure)

        if args.dry_run:
            print(f"\n{'='*60}")
            print(f"# 生成予定: {test_path}")
            print(content)
            generated += 1
        else:
            with open(test_path, "w", encoding="utf-8") as f:
                f.write(content)
            _update_index(index, failure, test_path)
            print(f"[GENERATED] {fid}: {os.path.relpath(test_path, _PROJECT_ROOT)}")
            generated += 1

    if not args.dry_run:
        _save_index(index)
        print(f"\n[DONE] 生成: {generated}件 / スキップ: {skipped}件")
        print(f"       インデックス更新: {os.path.relpath(_REGRESSION_INDEX_PATH, _PROJECT_ROOT)}")
    else:
        print(f"\n[DRY-RUN] {generated}件のテストを生成する予定")


if __name__ == "__main__":
    main()
